    public void marks(){
         try {
            query = "SELECT * FROM question WHERE questionId='"+id5+"'";
            //stmt.setInt(1, user.getUserId());
            dbConnection dc=new dbConnection();
            PreparedStatement stmt = dc.conn.prepareStatement(query);
            ResultSet rs = stmt.executeQuery();
            while(rs.next()){
                cor5=(rs.getString("correct"));             
            }

        } catch (SQLException ex) {
            
        }
         
        if(cor5.equals(ans5)){
            finalmarks=1+marks1;
        }
        else{
            finalmarks=0+marks1;
        }
        String fm=Integer.toString(finalmarks);
        lblMarks.setText(fm);
        
        if(finalmarks<=2){
            lblMessege.setText("You have failed"+ " "+token +". "+ "Please try again!!!");
        }
        else{
            lblMessege.setText("Congratulation"+" "+token+". ");
        }
    }
    
